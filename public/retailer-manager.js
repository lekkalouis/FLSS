function render() {
  const q = searchEl.value.trim().toLowerCase();
  const provinceFilter = provinceEl.value;

  const filtered = retailers.filter((r) => {
    const provinceMatch = provinceFilter === "all" || r.province === provinceFilter;
    if (!provinceMatch) return false;
    if (!q) return true;
    return `${r.name} ${r.location} ${r.province}`.toLowerCase().includes(q);
  });

  markersLayer.clearLayers();
  for (const r of filtered) {
    const [lat, lng] = coordinateFor(r);
    L.circleMarker([lat, lng], {
      radius: 5,
      weight: 1,
      color: "#1d4ed8",
      fillColor: "#3b82f6",
      fillOpacity: 0.75
    })
      .bindPopup(`<b>${r.name}</b><br>${r.location}<br>${r.province}`)
      .addTo(markersLayer);
  }

  listEl.innerHTML = "";
  for (const r of filtered.slice(0, 300)) {
    const li = document.createElement("li");
    li.innerHTML = `<div class="name">${r.name}</div><div class="meta">${r.location} · ${r.province}</div>`;
    listEl.append(li);
  }

  if (filtered.length > 300) {
    const li = document.createElement("li");
    li.className = "meta";
    li.textContent = `Showing first 300 of ${filtered.length} retailers in list. All are still mapped.`;
    listEl.append(li);
  }
}

searchEl.addEventListener("input", render);
provinceEl.addEventListener("change", render);
