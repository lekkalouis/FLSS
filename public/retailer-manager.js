function parseDirectory(text) {
  const lines = text.split(/\r?\n/).map((line) => line.trim()).filter(Boolean);
  const entries = [];
  let currentProvince = "";
  let currentLocation = "";

  for (const line of lines) {
    const provinceMatch = line.match(/^([A-Z\-\s]+)\s*\((\d+)\s*Stores?\)/i);
    if (provinceMatch) {
      currentProvince = provinceMatch[1].trim().toUpperCase();
      currentLocation = "";
      continue;
    }

    const locationMatch = line.match(/^(.+?)\s*\((\d+)\)$/);
    if (locationMatch && !line.startsWith("-")) {
      currentLocation = locationMatch[1].trim();
      continue;
    }

    if (line.startsWith("-")) {
      const retailer = line.replace(/^-\s*/, "").trim();
      if (retailer && currentProvince) {
        entries.push({
          name: retailer,
          location: currentLocation || "Unknown location",
          province: currentProvince
        });
      }
    }
  }

  return entries;
}

function hashCode(str) {
  let h = 0;
  for (let i = 0; i < str.length; i += 1) h = (h << 5) - h + str.charCodeAt(i);
  return Math.abs(h);
}

function coordinateFor(entry) {
  const base = PROVINCE_COORDS[entry.province] || [-29.0, 24.0];
  const h = hashCode(`${entry.location}|${entry.name}`);
  const rLat = ((h % 1000) / 1000 - 0.5) * 1.2;
  const rLng = ((((h / 1000) | 0) % 1000) / 1000 - 0.5) * 1.2;
  return [base[0] + rLat, base[1] + rLng];
}

